    private void mostrar(){
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = Database.getTabla("SELECT * FROM prueba");
        modelo.setColumnIdentifiers(new Object[]{"id", "nombre","apaterno","area","correo_electronico","fecha_solicitud","telefono","reporte","status"});
        try{
            while (rs.next()){
                modelo.addRow(new Object[]{rs.getInt("id"), rs.getString("nombre"), rs.getString("apaterno"), rs.getString("area"),rs.getString("correo_electronico"), rs.getString("fecha_solicitud"), rs.getString("telefono"), rs.getString("reporte"), rs.getString("status")});
            }
            table.setModel(modelo);
        }catch(Exception e){
            System.out.println(e);
        }
    }
